public abstract class Layout {
    public static interface LayoutCreator {
        public Layout create();

        public String getName();
    }

    protected Widget root;
    protected List<Widget> children;

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public Layout() {

    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------

    public Widget getRoot() {
        return root;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------

    public void setRoot(Widget root) {
        this.root = root;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------

    public List<Widget> getChildren() {
        return children;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------

    public void setChildren(List<Widget> children) {
        this.children = children;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------

    /**
     * Given the root widget and the widgets to pack, this method calculates each widgets dimensions based on the layout
     * type, root widgets dimensions, order and widgets position preferences.
     */
    public abstract void pack();

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public abstract String getName();
}
