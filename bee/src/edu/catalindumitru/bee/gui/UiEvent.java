public class UiEvent {
    public enum TYPE {
        /*drawing events*/
        REDRAW_REGION,
        REDRAW_ALL,


        /*input events*/
        MOUSE_CLICK,
        MOUSE_DOWN,
        MOUSE_UP,
        MOUSE_MOVE,
        MOUSE_WHEEL,
        MOUSE_DOUBLE_CLICK,
        KEY_UP,
        KEY_DOWN
    }

    /*which region is affected by this event. null if not important or all the area is affected*/
    protected Rectangle regionAffected;
    /*event params*/
    protected float params[];
    /*type of event*/
    public TYPE type;

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------

    public UiEvent(Rectangle regionAffected, float[] params, TYPE type) {
        this.regionAffected = regionAffected;
        this.params = params;
        this.type = type;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------

    public Rectangle getRegionAffected() {
        return regionAffected;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------

    public void setRegionAffected(Rectangle regionAffected) {
        this.regionAffected = regionAffected;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------

    public float[] getParams() {
        return params;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------

    public void setParams(float[] params) {
        this.params = params;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public TYPE getType() {
        return type;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public void setType(TYPE type) {
        this.type = type;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------


}
