public class Rectangle {
    protected float x;
    protected float y;

    protected float width;
    protected float height;

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public Rectangle(float x, float y, float width, float height) {
        this.x = x;
        this.y = y;
        this.height = height;
        this.width = width;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public Rectangle(float width, float height) {
        this(0, 0, width, height);
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public Rectangle() {
        this(0, 0, 0, 0);
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public float getX() {
        return x;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public void setX(float x) {
        this.x = x;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public float getY() {
        return y;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public void setY(float y) {
        this.y = y;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public float getWidth() {
        return width;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public void setWidth(float width) {
        this.width = width;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public float getHeight() {
        return height;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public void setHeight(float height) {
        this.height = height;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public void setAll(float x, float y, float width, float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public boolean contains(Point2D point) {
        return  (point.getX() > this.x
                && point.getX() < (this.x + this.width)
                && point.getY() > this.y
                && this.getY() < (this.y + this.height));
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public boolean contains(Rectangle rectangle) {
        return (rectangle.x > this.x
                && (rectangle.x + rectangle.width) < (this.x + this.width)
                && rectangle.y > this.y
                && (rectangle.y + rectangle.height) < (this.y + this.height));
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public boolean intersects(Rectangle rectangle) {
        return ((rectangle.x + rectangle.width) > this.x
                && (rectangle.x < (this.x + this.width))
                && (rectangle.y + rectangle.height) > this.y
                && (rectangle.y < (this.y + this.height)));
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
}
