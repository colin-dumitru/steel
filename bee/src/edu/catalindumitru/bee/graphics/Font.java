public class Font {
    public enum STYLE {NORMAL, ITALIC, OBLIQUE}
    public enum WEIGHT {NORMAL, BOLD}

    /*font params*/
    protected String family;
    protected float size;
    protected float height;
    protected STYLE style;
    protected WEIGHT weight;

    public static final Font DEFAULT = new Font("Arial", 1.0f, 1.0f, STYLE.NORMAL, WEIGHT.NORMAL);

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------

    public Font(String family, float size, float height, STYLE style, WEIGHT weight) {
        this.family = family;
        this.size = size;
        this.height = height;
        this.style = style;
        this.weight = weight;
    }

    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public Font() {
        this("Times New Roman", 1, 1, STYLE.NORMAL, WEIGHT.NORMAL);
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public Font(String family, float size, STYLE style, WEIGHT weight) {
        this(family, size, size, style, weight);
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public Font(String family, float size) {
        this(family, size, size, STYLE.NORMAL, WEIGHT.NORMAL);
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public Font (String family) {
        this(family, 1, 1, STYLE.NORMAL, WEIGHT.NORMAL);
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------


    public String getFamily() {
        return family;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public void setFamily(String family) {
        this.family = family;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public float getSize() {
        return size;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public void setSize(float size) {
        this.size = size;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public float getHeight() {
        return height;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public void setHeight(float height) {
        this.height = height;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public STYLE getStyle() {
        return style;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public void setStyle(STYLE style) {
        this.style = style;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public WEIGHT getWeight() {
        return weight;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
    public void setWeight(WEIGHT weight) {
        this.weight = weight;
    }
    //------------------------------------------------------------------------------------------------------------------
    //------------------------------------------------------------------------------------------------------------------
}
